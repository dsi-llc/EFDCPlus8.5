    CLOSE(90)
    CLOSE(94)
    CLOSE(99)
  ENDIF
  992 FORMAT(2I5,2X,F6.2,'    ! I,J = ',2I5,' FROM LAYER = ',I5,' TO LAYER = ',I5)
