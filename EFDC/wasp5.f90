  !!!!!!!!!CHANGES NEXT 12 LINES
  !
      IF( SUBO(L) == 1. )THEN
        FLOWX=DYU(L)*(UHLPF(L,K)+SVPT*UVPT(L,K))*DZC(L,K)
        IMTMP=I-1
