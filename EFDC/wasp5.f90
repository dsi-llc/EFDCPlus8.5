            WRITE(94,941)LUTM,LDTM
            WRITE(99,991)LUTM,LDTM,RADVMOD,I,J,IP1,J,KLAYTMP
          ENDIF
        ENDIF
      ENDDO
  !
  !!!!!!!!!CHANGES NEXT 41 LINES
  !
      DO LT=2,LALT
        I=ILLT(LT)
        J=JLLT(LT)
        JM1=J-1
        JP1=J+1
        L=LIJ(I,J)
        IF( SVBO(L) == 1. )THEN
          LSLT=LSCLT(LT)
          LDTM=LT-1+KMUL*LCLTM2
          LUTM=LSLT-1+KMUL*LCLTM2
          IF( IJCTLT(I,J-1) == 8 ) LUTM=0
          RLENTH=DYV(L)
          WIDTH=DXV(L)
          LCHN=LCHN+1
  !
  !           LCHNC(LDTM,NCHNC(LDTM))=LCHN
  !           LCHNC(LUTM,NCHNC(LUTM))=LCHN
  !
          IF( ISDHD == 1 ) WRITE(90,902)LCHN,RLENTH,WIDTH,RMNDUM, &
              LUTM,LDTM
          WRITE(94,941)LUTM,LDTM
          WRITE(99,991)LUTM,LDTM,RADVMOD,I,JM1,I,J,KLAYTMP
        ENDIF
        IF( IJCTLT(I,J+1) == 8 )THEN
          LN=LNC(L)
          IF( SVBO(LN) == 1. )THEN
            LSLT=LSCLT(LT)
            LDTM=0
            LUTM=LT-1+KMUL*LCLTM2
            RLENTH=DYV(LN)
            WIDTH=DXV(LN)
            LCHN=LCHN+1
  !
  !             LCHNC(LDTM,NCHNC(LDTM))=LCHN
  !             LCHNC(LUTM,NCHNC(LUTM))=LCHN
  !
            IF( ISDHD == 1 ) WRITE(90,902)LCHN,RLENTH,WIDTH,RMNDUM, &
