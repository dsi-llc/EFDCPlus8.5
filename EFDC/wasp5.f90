            WRITE(94,941)LUTM,LDTM
            WRITE(99,991)LUTM,LDTM,RADVMOD,I,J,I,JP1,KLAYTMP
          ENDIF
        ENDIF
      ENDDO
    ENDDO
    IF( KC > 1 )THEN
      DO K=KS,1,-1
        KMUL1=KS-K
        KMUL2=KMUL1+1
        KFROM=KMUL1+1
        KT000=KFROM+1
        DO LT=2,LALT
          I=ILLT(LT)
          J=JLLT(LT)
          L=LIJ(I,J)
          IF( SWB(L) == 1. )THEN
            LUTM=LT-1+KMUL1*LCLTM2
            LDTM=LT-1+KMUL2*LCLTM2
            RLENTH=HLPF(L)*DZG(L,K)
            WIDTH=SQRT(DXYP(L))
            LCHN=LCHN+1
  !
  !             LCHNC(LDTM,NCHNC(LDTM))=LCHN
  !             LCHNC(LUTM,NCHNC(LUTM))=LCHN
  !
            IF( ISDHD == 1 ) WRITE(90,902)LCHN,RLENTH,WIDTH,RMNDUM, &
                LUTM,LDTM
            WRITE(94,941)LUTM,LDTM
            WRITE(99,992)LUTM,LDTM,RADVMOD,I,J,KFROM,KT000
          ENDIF
        ENDDO
      ENDDO
    ENDIF
  !
  ! **  JUNCTION DATA WITH INITIAL CONDITIONS
  !
    VELTMP=0.
    DUMVOL=0.
    DO K=KC,1,-1
      KMUL=KC-K
      DO LT=2,LALT
        I=ILLT(LT)
        J=JLLT(LT)
        L=LIJ(I,J)
        LCELL=LT-1+KMUL*LCLTM2
        DEPTMP=HLPF(L)*DZC(L,K)
        VOLTMP=DEPTMP*DXYP(L)
        IF( ISDHD == 1 ) WRITE(90,904)LCELL,VOLTMP,I,J
        WRITE(94,9440)VOLTMP,DUMVOL,DEPTMP,VELTMP
      ENDDO
