        IF( ISDHD == 1 ) WRITE(90,944)LCHNUM,FLOWY,I,IPTMP,J,K
        WRITE(94,946)FLOWY
      ENDIF
      IF( IJCTLT(I,J+1) == 8 )THEN
        LN=LNC(L)
        IF( SVBO(LN) == 1. )THEN
          FLOWY=DXV(LN)*(VHLPF(LN,K)+SVPT*VVPT(LN,K))*DZC(L,K)
          JPTMP=J+1
          LCNHUM=LCHNUM+1
          IF( ISDHD == 1 ) WRITE(90,944)LCHNUM,FLOWY,I,IPTMP,J,K
          WRITE(94,946)FLOWY
        ENDIF
      ENDIF
    ENDDO
  ENDDO
  IF( KC > 1 )THEN
    DO K=KS,1,-1
      DO LT=2,LALT
        I=ILLT(LT)
        J=JLLT(LT)
        L=LIJ(I,J)
        IF( SWB(L) == 1 )THEN
          FLOWZ=-DXYP(L)*(WLPF(L,K)+SVPT*WVPT(L,K))
          KPTMP=K+1
          LCNHUM=LCHNUM+1
          IF( ISDHD == 1 ) WRITE(90,944)LCHNUM,FLOWZ,I,IPTMP,J,K
          WRITE(94,946)FLOWZ
        ENDIF
      ENDDO
    ENDDO
  ENDIF
  QQSUM=0.
  LCELTMP=0
  DO K=KC,1,-1
    DO LT=2,LALT
      LCELTMP=LCELTMP+1
      I=ILLT(LT)
      J=JLLT(LT)
      L=LIJ(I,J)
      LN=LNC(L)
